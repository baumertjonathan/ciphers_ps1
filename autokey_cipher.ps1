                if ($tabula_recta[$j][$k_int] -eq $Text[$i]){
                    $p_int = $j
                }
            }
            $result += $alphabet[$p_int];
            $Key += $alphabet[$p_int];
        }
        Write-Host($Key);
    }
    #Return
    return $result;
}